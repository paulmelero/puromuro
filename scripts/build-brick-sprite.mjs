/**
 * build-brick-sprite.mjs
 *
 * Merges all individual SVG brick frames into a single SVG sprite file
 * using <defs> + <symbol> elements.
 *
 * Input:  public/images/uploads/design/pm_ladrillo_SVG/Recurso *.svg (72 frames)
 * Output: public/images/uploads/design/pm_ladrillo_sprite.svg
 *
 * Run with: node scripts/build-brick-sprite.mjs
 */

import { readFileSync, writeFileSync, readdirSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = join(__dirname, '..')

const SRC_DIR = join(root, 'public/images/uploads/design/pm_ladrillo_SVG')
const OUT_FILE = join(root, 'public/images/uploads/design/pm_ladrillo_sprite.svg')

// Sort by the numeric suffix in "Recurso N.svg"
const files = readdirSync(SRC_DIR)
  .filter((f) => f.endsWith('.svg'))
  .sort((a, b) => {
    const numA = parseInt(a.match(/(\d+)/)?.[1] ?? '0', 10)
    const numB = parseInt(b.match(/(\d+)/)?.[1] ?? '0', 10)
    return numA - numB
  })

if (files.length === 0) {
  console.error('No SVG files found in', SRC_DIR)
  process.exit(1)
}

console.log(`Found ${files.length} SVG frames. Building sprite...`)

const symbols = files.map((file, i) => {
  const raw = readFileSync(join(SRC_DIR, file), 'utf8')

  // Extract the inner content of the main group
  const groupMatch = raw.match(/<g[^>]*id="Capa_1-2"[^>]*>([\s\S]*?)<\/g>/)
  if (!groupMatch) {
    console.warn(`  [warn] Could not find <g id="Capa_1-2"> in ${file} — skipping`)
    return `<symbol id="brick-frame-${i}" viewBox="0 0 251.92 213.34" fill="currentColor"></symbol>`
  }

  let inner = groupMatch[1]

  // Remove invisible boundary rects (<rect class="cls-1" .../>)
  inner = inner.replace(/<rect[^>]*class="cls-1"[^>]*\/?>/g, '')

  // Remove any leftover whitespace-only lines
  inner = inner.replace(/^\s*[\r\n]/gm, '').trim()

  return `<symbol id="brick-frame-${i}" viewBox="0 0 251.92 213.34" fill="currentColor">\n  ${inner}\n</symbol>`
})

const sprite = `<?xml version="1.0" encoding="UTF-8"?>
<!-- Auto-generated by scripts/build-brick-sprite.mjs — do not edit manually -->
<!-- ${files.length} animation frames (Recurso 63–134) -->
<svg xmlns="http://www.w3.org/2000/svg" aria-hidden="true" style="display:none">
  <defs>
${symbols.map((s) => '    ' + s.replace(/\n/g, '\n    ')).join('\n')}
  </defs>
</svg>
`

writeFileSync(OUT_FILE, sprite, 'utf8')
console.log(`Done! Sprite written to: ${OUT_FILE}`)
console.log(`  Frames: ${files.length}`)
console.log(`  Size:   ${(sprite.length / 1024).toFixed(1)} KB`)
